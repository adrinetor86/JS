class Alumno{

    nombre;
    apellidos;
    edad;
    postre;
    asignatura;


    constructor(nombre,apellidos,edad,postre,asignatura){
        this.nombre=nombre;
        this.apellidos=apellidos;
        this.edad=edad;
        this.postre=postre;
        this.asignatura=asignatura;
    }

}
export {Alumno};
